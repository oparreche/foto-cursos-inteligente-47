
import { supabase } from '@/integrations/supabase/client';

export async function findUserByEmail(email: string): Promise<string | undefined> {
  try {
    // Store response in a variable without type annotation to avoid TS analyzing it
    const response = await supabase
      .from('profiles')
      .select('id')
      .eq('email', email)
      .limit(1)
      .single();
      
    // Extract only what we need from the response
    const rawData = response as Record<string, any>;
    if (rawData.error) throw rawData.error;
    
    return rawData.data?.id;
  } catch (error) {
    console.error('Error finding user by email:', error);
    return undefined;
  }
}

export async function createUser(
  email: string,
  firstName: string,
  lastName: string,
  profileData: {
    cpf?: string;
    birthDate?: string;
    phone?: string;
    address?: string;
    addressNumber?: string;
    addressComplement?: string;
    neighborhood?: string;
    city?: string;
    state?: string;
    postalCode?: string;
  }
): Promise<string | undefined> {
  try {
    // First check if user already exists
    const existingUserId = await findUserByEmail(email);
    if (existingUserId) return existingUserId;
    
    // Create profile entry with UUID generated by Supabase
    const profileId = crypto.randomUUID();
    
    // Use raw response approach to avoid TypeScript errors
    const response = await supabase
      .from('profiles')
      .insert({
        id: profileId,
        email: email,
        first_name: firstName,
        last_name: lastName,
        cpf: profileData.cpf,
        birth_date: profileData.birthDate,
        phone: profileData.phone,
        address: profileData.address,
        address_number: profileData.addressNumber,
        address_complement: profileData.addressComplement,
        neighborhood: profileData.neighborhood,
        city: profileData.city,
        state: profileData.state,
        postal_code: profileData.postalCode
      });
    
    // Extract just the error property we need
    const rawResponse = response as Record<string, any>;
    if (rawResponse.error) throw rawResponse.error;
    
    return profileId;
  } catch (error) {
    console.error('Error creating user:', error);
    return undefined;
  }
}
